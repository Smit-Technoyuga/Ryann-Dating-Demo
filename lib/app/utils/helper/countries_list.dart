class CountryCode {
  const CountryCode({
    required this.dialCode,
    required this.countryCode,
    required this.e164Sc,
    required this.geographic,
    required this.level,
    required this.name,
    required this.example,
    required this.displayName,
    this.fullExampleWithPlusSign,
    required this.displayNameNoE164Cc,
    required this.e164Key,
  });

  /// Create CountryCode from JSON map
  factory CountryCode.fromJson(Map<String, dynamic> json) {
    return CountryCode(
      dialCode: json['e164_cc'] as String,
      countryCode: json['iso2_cc'] as String,
      e164Sc: json['e164_sc'] as int,
      geographic: json['geographic'] as bool,
      level: json['level'] as int,
      name: json['name'] as String,
      example: json['example'] as String,
      displayName: json['display_name'] as String,
      fullExampleWithPlusSign: json['full_example_with_plus_sign'] as String?,
      displayNameNoE164Cc: json['display_name_no_e164_cc'] as String,
      e164Key: json['e164_key'] as String,
    );
  }

  /// E.164 country calling code (without + sign)
  final String dialCode;

  /// ISO 3166-1 alpha-2 country code
  final String countryCode;

  /// E.164 assigned country code shared code (0 if not shared)
  final int e164Sc;

  /// Whether this is a geographic region
  final bool geographic;

  /// Level of country (1-3)
  final int level;

  /// Country name
  final String name;

  /// Example phone number (without country code)
  final String example;

  /// Display name with country code
  final String displayName;

  /// Full example with plus sign and country code
  final String? fullExampleWithPlusSign;

  /// Display name without country code
  final String displayNameNoE164Cc;

  /// E.164 key identifier
  final String e164Key;

  /// Convert CountryCode to JSON map
  Map<String, dynamic> toJson() {
    return {
      'e164_cc': dialCode,
      'iso2_cc': countryCode,
      'e164_sc': e164Sc,
      'geographic': geographic,
      'level': level,
      'name': name,
      'example': example,
      'display_name': displayName,
      'full_example_with_plus_sign': fullExampleWithPlusSign,
      'display_name_no_e164_cc': displayNameNoE164Cc,
      'e164_key': e164Key,
    };
  }

  /// Get formatted phone number with country code
  String getFormattedExample() {
    return fullExampleWithPlusSign ?? '+$dialCode$example';
  }

  /// Check if country code matches
  bool matchesCountryCode(String code) {
    return dialCode == code || countryCode.toLowerCase() == code.toLowerCase();
  }

  @override
  String toString() {
    return 'CountryCode(name: $name, code: +$dialCode, iso: $countryCode)';
  }

  @override
  bool operator ==(Object other) {
    if (identical(this, other)) return true;
    return other is CountryCode &&
        other.dialCode == dialCode &&
        other.countryCode == countryCode &&
        other.e164Sc == e164Sc;
  }

  @override
  int get hashCode => Object.hash(dialCode, countryCode, e164Sc);
}

String countryCodeToEmoji(String countryCode) {
  // 0x41 is Letter A
  // 0x1F1E6 is Regional Indicator Symbol Letter A
  // Example :
  // firstLetter U => 20 + 0x1F1E6
  // secondLetter S => 18 + 0x1F1E6
  // See: https://en.wikipedia.org/wiki/Regional_Indicator_Symbol
  final firstLetter = countryCode.codeUnitAt(0) - 0x41 + 0x1F1E6;
  final secondLetter = countryCode.codeUnitAt(1) - 0x41 + 0x1F1E6;
  return String.fromCharCode(firstLetter) + String.fromCharCode(secondLetter);
}

